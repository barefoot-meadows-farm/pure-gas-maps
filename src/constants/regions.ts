export function getKmlUrl(regionCode: string): string {
  const base = import.meta.env.DEV
    ? '/kml-proxy/temp'
    : 'https://www.pure-gas.org/temp'
  return `${base}/pure-gas-${regionCode.toLowerCase()}.kml`
}
